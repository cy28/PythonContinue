# 1. str --> int 这个字符串的内容必须是整形
# 2. str --> float 这个字符串的内容必须是浮点型

# 3. int --> str      str(a)
# 4. float --> str

# 5. int --> float    float(a)
# 6. float --> int    int(a)  小数点后面被抹掉

